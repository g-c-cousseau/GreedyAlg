public class GreedyAlgItem
{
    public String name;
    public float weight;
    public float value;
    public float density;
    
    public GreedyAlgItem(String name, float weight, float value, float density)
    {
        this.name = name;
        this.weight = weight;
        this.value = value;
        this.density = density;
    }

}